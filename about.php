				<h2>About NSS</h2> <br/>
				<h3>Introduction</h3>
                <p align="justify">National Service Scheme (NSS) is a permanent youth programme under the ministry of Youth Affairs and Sports, Government of India and funded by Government of Tamil Nadu and Government of India in the ratio of 5:7 </p>
					<h3>Objective of NSS</h3>
                <p align="justify">Development of personality of students through community service. </p>
				<h3>Motto</h3>
				<p align="justify">The motto of NSS is "NOT ME BUT YOU". It underlines that the welfare of an individual is ultimately dependent on the welfare of the society as a whole. This expresses the essence of democratic living and upholds the need of selfness service and appreciation of the other man's point of view and also consideration for fellow human beings. </p>                
                <h3>Symbol</h3>
                <p align="justify">The NSS symbol is based on the "Rath Wheel" of the Konark Sun Temple of Orissa. The giant wheel portrays the cycles of creation, preservation and release and signifies the movement in life across time and space. The design of the symbol, a simplified form of Sun Chariot Wheel primarily depicts movement. The wheel signifies the progressive cycle of life. It also stands for dynamism and progressive outlook of youth.  </p>
                <h3>NSS Unit & Programme Officer</h3>
                <p align="justify">One NSS Unit consists of 100 student volunteer guided by one Programme Officer who will be a faculty member. The Programme Officer is expected to motivate the student youth to understand the values and philosophy of NSS. The overall functions of Programme Officer are to help the students to plan, implement and evaluate the activities of NSS under his/her charge and give proper guidance and directions to the student volunteers.</p>
                <h3>NSS Student Volunteers</h3>
                <p align="justify">Any student enrolled as a NSS volunteer, as per NSS Manual should put in atleast 240 hours of useful social work in a continuous period of two years (i.e. 120 hours per year). A work diary is to be maintained by each NSS volunteer, which will help him/her in the assessment in his/her performance. Such volunteer is eligible to get NSS Service Certificate from the colleges. The Service Certificate given in the NSS Manual should be strictly adhered. </p>
				<h3>Qualifications for the Programme Officer</h3>
                <p align="justify">a. Programme officer will be selected from the members of teaching faculty only. <br/>
b. NCC officers and Physical Education Directors should not be appointed as NSS Programme Officers. <br/>  
c. In Women's Colleges lady teachers should be appointed as Programme Officers. However, male members may help the lady Programme Officers.    <br/> 
d. A teacher who has high level of motivation, inclination and attitude for community work and above all, very good rapport with students should be preferred as Programme Officer.  </p>
				<h3>NSS Theme for the Year-2008-2009</h3>
				<p align="justify">Every year the Government of India a theme based on which the NSS activities of the year shall be carried out. <br/>
                  <span class="style1">HEALTHY YOUTH FOR HEALTH INDIA</span> <br/> 
Under this theme all the health related issues may be covered which include General Health, Mental health, Spiritual Health, HIV/AIDS, Sexual Abuse, Adolescent Health and Population Education. This theme will be continued for the next 2 years.</p> 
				   <h3>Activities of NSS Volunteers and Adoption of Village</h3>
